def logo():
    colors = [
        "\033[38;5;93m",  # purple
        "\033[38;5;99m",
        "\033[38;5;105m",
        "\033[38;5;111m",
        "\033[38;5;117m",
        "\033[38;5;123m",
        "\033[38;5;129m",  # teal
    ]

    lines = [
        "                                                       ",
        "                                                       ",
        "                                                       ",
        "                                                       ",
        "                                                    ",
        " /$$$$$$$                     /$$   /$$              ",
        "| $$__  $$                   |__/  | $$              ",
        "| $$  \\ $$ /$$$$$$   /$$$$$$  /$$ /$$$$$$   /$$   /$$",
        "| $$$$$$$/|____  $$ /$$__  $$| $$|_  $$_/  | $$  | $$",
        "| $$____/  /$$$$$$$| $$  \\__/| $$  | $$    | $$  | $$",
        "| $$      /$$__  $$| $$      | $$  | $$ /$$| $$  | $$",
        "| $$     |  $$$$$$$| $$      | $$  |  $$$$/|  $$$$$$$",
        "|__/      \\_______/|__/      |__/   \\___/   \\____  $$",
        "                                            /$$  | $$",
        "                                           |  $$$$$$/",
        "                                            \\______/",
        " Created by Tom White and Xavier Barinaga            "
    ]

    art = ""
    for i, line in enumerate(lines):
        color = colors[i % len(colors)]
        art += f"{color}{line}\033[0m\n"
    return art
